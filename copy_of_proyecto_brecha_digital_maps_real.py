import json
import pandas as pd
import numpy as np
import requests
import folium
from sklearn.ensemble import RandomForestClassifier
from sklearn.model_selection import train_test_split
from sklearn.metrics import classification_report, confusion_matrix

"""# 2) Dataset real (INE 2024)

Valores de personas que han utilizado Internet en los últimos 3 meses (% de población de 16 a 74 años).
"""

internet_data = [
    ("Andalucía", 95.0),
    ("Aragón", 96.2),
    ("Asturias", 92.8),
    ("Balears", 96.3),
    ("Canarias", 96.1),
    ("Cantabria", 94.3),
    ("Castilla y León", 94.7),
    ("Castilla-La Mancha", 95.9),
    ("Cataluña", 97.0),
    ("Comunitat Valenciana", 95.8),
    ("Extremadura", 92.1),
    ("Galicia", 92.5),
    ("Madrid", 97.7),
    ("Murcia", 95.4),
    ("Navarra", 95.7),
    ("País Vasco", 95.8),
    ("La Rioja", 94.3),
    ("Ceuta", 95.2),
    ("Melilla", 96.4),
]

df = pd.DataFrame(internet_data, columns=["ccaa", "uso_internet_2024"])
df

"""## 3) Crear variables derivadas

- `brecha_digital`: cuanto más alta, peor acceso relativo.
- `prioridad`: clasificación para ayudas tecnológicas.
"""

df["brecha_digital"] = 100 - df["uso_internet_2024"]

def clasificar_prioridad(x):
    if x >= 7:
        return "Urgente"
    elif x >= 4:
        return "Media"
    else:
        return "Baja"

df["prioridad"] = df["brecha_digital"].apply(clasificar_prioridad)
df.sort_values("brecha_digital", ascending=False)

"""## 4) Cargar GeoJSON de comunidades autónomas

Se usa un GeoJSON público simplificado para visualización rápida en Colab.
"""

geojson_url = "https://raw.githubusercontent.com/codeforgermany/click_that_hood/main/public/data/spain-communities.geojson"

"""## 5) Unificar nombres para que el mapa case bien

Mapeo entre nombres en el GeoJSON (claves) y nombres en el DataFrame/INE (valores).
"""

name_map = {
    "Andalucia": "Andalucía",
    "Aragon": "Aragón",
    "Asturias": "Asturias",
    "Baleares": "Balears",
    "Canarias": "Canarias",
    "Cantabria": "Cantabria",
    "Castilla-Leon": "Castilla y León",
    "Castilla-La Mancha": "Castilla-La Mancha",
    "Cataluña": "Cataluña",
    "Valencia": "Comunitat Valenciana",
    "Extremadura": "Extremadura",
    "Galicia": "Galicia",
    "Madrid": "Madrid",
    "Murcia": "Murcia",
    "Navarra": "Navarra",
    "Pais Vasco": "País Vasco",
    "La Rioja": "La Rioja",
    "Ceuta": "Ceuta",
    "Melilla": "Melilla",
}

df_to_geojson_name_map = {v: k for k, v in name_map.items()}
df["ccaa_geojson_name"] = df["ccaa"].map(df_to_geojson_name_map)
unmapped = df["ccaa_geojson_name"].isna().sum()
assert unmapped == 0, f"CCAA sin mapear en GeoJSON: {unmapped}. Revisar name_map."

m = folium.Map(location=[40.2, -3.5], zoom_start=5, tiles="cartodbpositron")

choropleth = folium.Choropleth(
    geo_data=geojson_url,
    data=df,
    columns=["ccaa_geojson_name", "brecha_digital"],
    key_on="feature.properties.name",
    fill_color="YlOrRd",
    fill_opacity=0.8,
    line_opacity=0.3,
    legend_name="Brecha digital (100 - uso de Internet)",
    nan_fill_color="lightgray",
).add_to(m)

folium.LayerControl().add_to(m)
m

"""## 6) Mapa coroplético: brecha digital"""

# Fetch the GeoJSON data
response = requests.get(geojson_url)
geojson_data = json.loads(response.text)

# Merge 'brecha_digital' into GeoJSON properties for tooltip
for feature in geojson_data['features']:
    geojson_name = feature['properties']['name']
    matching_row = df[df['ccaa_geojson_name'] == geojson_name]

    if not matching_row.empty:
        brecha = matching_row['brecha_digital'].iloc[0]
        feature['properties']['brecha_digital'] = brecha
    else:
        feature['properties']['brecha_digital'] = None # Handle unmatched features if any


m = folium.Map(location=[40.2, -3.5], zoom_start=5, tiles="cartodbpositron")

choropleth = folium.Choropleth(
    geo_data=geojson_data, # Use the modified GeoJSON dictionary
    data=df,
    columns=["ccaa_geojson_name", "brecha_digital"],
    key_on="feature.properties.name",
    fill_color="YlOrRd",
    fill_opacity=0.8,
    line_opacity=0.3,
    legend_name="Brecha digital (100 - uso de Internet)",
    nan_fill_color="lightgray",
).add_to(m)

# Add tooltips
choropleth.geojson.add_child(
    folium.features.GeoJsonTooltip(
        fields=['name', 'brecha_digital'],
        aliases=['Comunidad Autónoma', 'Brecha Digital (%)'],
        labels=True
    )
)

folium.LayerControl().add_to(m)
m

"""## 7) Tabla final de prioridades"""

df.sort_values(["prioridad", "brecha_digital"], ascending=[True, False])

"""## 8) Mini modelo de IA

Aquí hacemos un modelo sencillo de clasificación. Como el dataset es pequeño, el objetivo es **didáctico**:
- Entrada: porcentaje de uso de Internet
- Salida: prioridad de ayuda
"""

X = df[["uso_internet_2024"]]
y = df["prioridad"]

X_train, X_test, y_train, y_test = train_test_split(
    X, y, test_size=0.3, random_state=42, stratify=y
)

modelo = RandomForestClassifier(random_state=42, n_estimators=100)
modelo.fit(X_train, y_train)

pred = modelo.predict(X_test)

print("Matriz de confusión:")
print(confusion_matrix(y_test, pred))
print()
print("Informe de clasificación:")
print(classification_report(y_test, pred))

"""## 9) Predicción para una nueva comunidad o zona

Ejemplo: si una zona tuviera 93.0% de uso de Internet.
"""

nueva_zona = pd.DataFrame({"uso_internet_2024": [93.0]})
pred_nueva = modelo.predict(nueva_zona)[0]
print("Prioridad estimada:", pred_nueva)

